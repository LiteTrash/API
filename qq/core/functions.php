function check_qq($qq){
    if (!is_numeric($qq)){
        sysmsg("QQ号规则不合法");
    }elseif (strlen($qq) < 5){
        sysmsg("QQ号小于5位");
    }elseif (strlen($qq) > 10){
        sysmsg("QQ号大于10位");
    }
}